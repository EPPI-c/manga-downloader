def menu():
    print_art()
    while(True):
        option = -1
        while(not(option >= 0 and option <= 2)):
            print('Choose a option:')
            print('1 - Download new chapters from a magazine')
            print('2 - Create a Magazine with your currently reading mangas')
            print('3 - Download a manga')
            print('0 - Exit')
            option = input("-> ")
            print('\n')
        
            clear_screen()
            if(not option.isdigit()):
                continue
            
            option = int(option)

        if(option == 0):
            break
        elif(option == 1):
            choose_magazine()
        elif(option == 2):
            gen_magazine()
        else:
            choose_standalone_manga()
        clear_screen()

def choose_magazine():

    option = -1
    magazines = listdir(get_magazines_dir())
    while(not(option >= 0 and option <= len(magazines))):
        print('Choose a option:')
        magazines = [splitext(filename)[0] for filename in listdir("magazines")]
        for n,magazine in enumerate(magazines,1):
            print(f'{n} - {magazine}')
        print('0 - Return')
        option = input("-> ")
        clear_screen()
        if(not option.isdigit()):
            continue
        option = int(option)

    if(option == 0):
        return 0
    else:
        asyncio.run(download_magazine(magazines[option - 1]))


def choose_standalone_manga():
    
    option = 'a'
    while(option != ''):
        print('*** Just Enter 0 to cancel ***')
        print('Write the manga name, it doesn\'t have to be accurate:')
        option = input('->')
        if option == '0':
            break
        name = option
        print('Write the link, take care to the available sites')
        option = input('->')
        if option == '0':
            break
        link = option
        print('Write from which chapter download')
        option = input('->')
        if option == '0':
            break
        last_chapter = option
        if not (last_chapter.isdigit() and int(last_chapter) > 0):
            continue
        last_chapter = int(last_chapter) - 1

        data = {
            "mangas":{
                f"{name}": {
                    "last_chapter": last_chapter,
                    "link": [link],
                    "name": name,
                },
            },
            "name": "__manga",
        }
        
        with open(f'{get_magazines_dir()}/__manga.yaml', 'w') as f:
            yaml_dump(data, f)
        
        asyncio.run(download_magazine('__manga'))

        remove(f'{get_magazines_dir()}/__manga.yaml')
        
        break
    
    
async def download_magazine(magazine_file:str):
    PATH = join_path(get_magazines_dir(),f'{magazine_file}.yaml')
    if '_anilist' in magazine_file:
        update_magazine(magazine_file.removesuffix('_anilist'))
    magazine = await create_magazine(path=PATH)
    chapters = await magazine.get_all_chapters()
    await magazine.download(chapters, 'mangas')


